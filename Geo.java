public class Geo {
    private double lat;
    private double lng;
    private double eet;

    public double getEet() {
        return eet;
    }

    public void setEet(double eet) {
        this.eet = eet;
    }

    public double getLat() {
        return lat;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }

    public double getLng() {
        return lng;
    }

    public void setLng(double lng) {
        this.lng = lng;
    }
}
